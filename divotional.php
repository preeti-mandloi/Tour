  <h2>Places for the devotional</h2>
</div>
  <h3><b>1. Vaishno Devi Temple, Jammu Kashmir </b></h3>
    <div class="wp-block-image">
      <img src="images/vd.jpg" style="width:700px;height:300px;">
    </div>
       <p>From high up in the mountains, Mata Vaishno Devi calls out to her devotees… Situated on the Trikuta Hills in Jammu, the abode of Mata Vaishno is amongst the most popular pilgrimage in India. Experience an indescribable delight in taking an arduous trek that further leads to the caves where the goddess presides over and bestows countless blessings. Beautiful landscape, pervading divinity and thrilling adventure… this is one journey in India that will remain etched in your memories forever.</p>

  <h3><b>2. Golden Temple</b></h3>
  <div class="wp-block-image">
      <img src="images/gt.jpg" style="width:700px;height:350px;">
  </div>
  <p>‘Set Your Eyes on the Vast Horizon and Divinity is all You can See’. The unparalleled reflection of the pious golden edifice on the ambrosial nectar (lake) and the horde of pilgrims loitering in the shrine complex urge one to visit this popular religious place in India. Built by the fourth Sikh Guru- Ramdass Sahib Ji, Gurdwara Harmandir Sahib is the most important Sikh pilgrimage site to visit in India. The golden gilded temple commemorates a number of Sikh historical events. A bringer of peace, the Golden Temple is nothing short of a legacy in India.
</p>
  <h3><b>3. Sun Temple (Surya Mandir) in Konark</b></h3>
  <div class="wp-block-image">
      <img src="images/st.jpg" style="width:700px;height:350px;">
  </div>
  <p>
    The masterpiece of Indian architecture, Sun Temple is the epitome of exquisite architecture and unmatched spirituality. Situated in Konark, Odisha, the Sun Temple boasts of a massive size and is an incredible example of genius. The Sun God sitting on a majestic chariot depicts the victory of King Narsimhadeva I (who defeated a number of Muslim invaders). The incredible architecture that includes 12 pairs of carved wheels, hall of offering, imposing pyramidal roof and erotic scenes have been the key driving force for art lovers and travel enthusiast who visit here, thereby making the Sun Temple one of the best places to see on a religious tour in India.</p>
 <h3><b>4. Somnath Jyotirlinga, Gujarat</b></h3>
  <div class="wp-block-image">
      <img src="images/sj.jpg" style="width:700px;height:350px;">
  </div>
  <p>Reckoned to be the first Dwadash Jyotirlinga (Pillar of Light) of Lord Shiva, Somnath Jyotirlinga in Gujarat holds an important place in India. The survivor of many attacks in the past, Somnath Jyotirlinga has been a source of divinity and spirituality for many Hindu pilgrims. Situated on the shores of Arabian Sea, this sacred shrine calls out to devotees from afar and blesses innumerable times. Feel the power of spirituality and lose yourself in the devotion at this holy place in India.</p>
 <h3><b>5. Tirumala Venkateswara Temple in Tirupati
</b></h3>
  <div class="wp-block-image">
      <img src="images/tiru.jpg" style="width:700px;height:350px;">
  </div>
  <p>The earthly abode of Lord Venkateswara (incarnation of Vishnu), Tirumala Venkateswara Temple is situated on the last hill of the Seshachalam range in Tirupati, Andhra Pradesh. Like many temples in south India, this holy temple is known for its exquisite architecture. It is also amongst the most visited Hindu temples in India as it is said that the temple attracts 60,000 pilgrims each day. One must attend the Puja that is accompanied by many hymns and praises giving it a touch of unmatched divinity. The Laddoo Prasadam is yet another attraction of Tirumala Venkateswara Temple.</p>
  <h3><b>6. Amarnath Cave</b></h3>
  <div class="wp-block-image">
      <img src="images/ac.jpg" style="width:700px;height:350px;">
  </div>
  <p>Baba Barfani calls out to his devotees spread all over the world to first take up an enthralling trek and then receive bountiful blessings from him. The holy Amarnath Cave is situated in the beautiful state of Jammu and Kashmir and can only be reached after a rigorous trekking expedition. The cave holds importance as one of the ancient pilgrimages in India. The legend has it that Lord Shiva revealed the secret of immortality to his consort Goddess Parvati here, which was overheard by a pair of pigeons. It is also believed that even today, a pair of pigeon can be seen in the cave. Amarnath is famed for the Linga that is created naturally by ice here every year. The Amarnath Yatra is an annual event in which pilgrims are allowed to trek to the cave temple.</p>
  <h3><b>7. Badrinath</b></h3>
  <div class="wp-block-image">
      <img src="images/bd.jpg" style="width:700px;height:350px;">
  </div>
  <p>Nestled in the pristine Himalaya, Badrinath in Uttarakhand is a door to unmatched spirituality. It is the place where everything is cuddled by divinity. Badrinath is believed to be the home of Lord Vishnu or Badrinathji, who had come here to meditate after being rebuked by Narad for being too indulgent in worldly affairs. Thus, today Badrinath offers confinement from the materialistic world and takes you a step closer to the Supreme Power. It is the vital destination for Chardham yatra in India and Chhota Chardham in Uttarakhand.</p>
  <h3><b>8. Rameshwaeram</b></h3>
  <div class="wp-block-image">
      <img src="images/rm.jpg" style="width:700px;height:350px;">
  </div>
  <p>Rameshwaeram in the state of Tamil Nadu is one of the sites of the pious Char Dham yatra in India. Situated on an island called Mannar, this sacred destination is believed to be the place where Lord Rama worshipped and repented after killing Raavana. Spirituality is set deep in Rameshwaram and the best place to experience it is at Ramanathaswamy temple. This beautiful temple is known for its incredible architecture that weaves divinity and culture together.</p>
  <h3><b>9. Haridwar</b></h3>
  <div class="wp-block-image">
      <img src="images/hd.jpg" style="width:700px;height:350px;">
  </div>
  <p>Haridwar, located in the lap of mystical Uttarakhand is nothing short of an empyrean for the Hindus. Located by the holy river Ganga, the sacred town offers a great peace of mind. Watch the flickering Diyas floating on the sparkling river along with the energetic Ganga Aarti in the evening and loiter around Har ki Pauri to seek unmatched divinity in Haridwar. The Kumbh Mela, which happens once in every 12 years attracts millions to the city; nonetheless the city remains crowded with devotees all year round.

</p>
