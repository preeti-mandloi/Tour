  <h3><b>1. Kanha National Park </b></h3>
    <div class="wp-block-image">
      <img src="images/ppp.jpg" style="width:700px;height:300px;">
    </div>
       <p>Kanha National Park is a paradise for wildlife lovers. With a core zone of 363 square miles, it’s the largest national park in Central India. It’s also considered one of the most well-managed. The result is a beautiful park that’s teeming with wildlife. With approximately 105 Bengal tigers, it’s a great place to spot the big cats in the wild. But there’s so much else to see there, including leopards, sloth bears, sambar and barasingha deer. Kanha’s breeding program played a very important role in saving the barasingha from extinction.   </p>

  <h3><b>2. Bandhavgarh National Park</b></h3>
  <div class="wp-block-image">
      <img src="images/np.jpg" style="width:700px;height:350px;">
  </div>
  <p>Bandhavgarh is very picturesque, and popular with photographers and wildlife enthusiasts. It’s always on the lists of the top national parks in India. The ruins of an ancient fort makes for a great backdrop to this park, with a core area of 40 square miles and a buffer area of approximately 154 square miles. It’s one of the best places to spot a tiger in India. Located among the Vindya Hills in Madhya Pradesh, Bandhavgarh boasts one of the highest densities of Bengal tigers in the world. But the magnificent cat is not the only attraction here. There are also 36 other mammals, including leopards, chital, dhole and nilgai (“blue bulls”), more than 150 species of birds, and around 80 species of butterflies.
</p>
  <h3><b>3. Kaziranga National Park</b></h3>
  <div class="wp-block-image">
      <img src="images/bn.jpg" style="width:700px;height:350px;">
  </div>
  <p>
    Located in the northeast state of Assam, Kaziranga is one of the finest wildlife refuges in the world. Home to the world’s largest population of one-horned rhinos, the park is an undisturbed natural area of wet grasslands, swamps, and pools in the Brahmaputra Valley floodplain. On the UNESCO Natural Heritage list for India, Kaziranga is a great conservation success story, having saved the one-horned rhino from the brink of extinction. In 1903, there were only 12 left in the region; now there are about 1,800.The park also harbors several other endangered species, such as Bengal tigers, Asian elephants, sloth bears, Gangetic dolphin, and many migratory birds. It’s the only park in India where elephant-back safaris are still deemed acceptable, as it’s the only way to view wildlife in the wet grasslands.</p>
 <h3><b>4. Nagarhole National Park</b></h3>
  <div class="wp-block-image">
      <img src="images/nn.jpg" style="width:700px;height:350px;">
  </div>
  <p>Nagarhole is Karnataka’s leading national park and wildlife getaway. Bounded by the pristine Kabini River and part of the Nilgiri biosphere, Nagarhole was formerly the hunting grounds of the Maharajah of Mysore and was declared a tiger reserve in 1999. This region is home to the largest concentration of herbivores in Asia, and the largest congregation of Asiatic elephants in the world. Tigers, leopards, sloth bears and dhole (wild dogs) also roam these enchanting forests. A year-round temperate climate and incredible wildlife viewing opportunities make Nagarhole a true nature lover’s paradise.</p>
 <h3><b>5. Periyar National Park, Kerala</b></h3>
  <div class="wp-block-image">
      <img src="images/pn.jpg" style="width:700px;height:350px;">
  </div>
  <p>Nestled on the Western Ghats, the Periyar National Park is the only wildlife sanctuary in South India as well as in India that has an artificial lake flowing through the forests. It is quite popular for its sheer beauty and tourism. It is also a popular Project Tiger Reserve, and also offers jeep safaris for the tourists. A boat ride on the beautiful Periyar Lake will enhance the fun.Some popular things to do in and around Periyar National Park include Fruitful Nature Walks, Elephant Safari, Tiger Trailing, Boat Cruise and the trip to Cardamom Hills. Also make your stay lavish at Top 10 Luxury Jungle Resorts Near Periyar National Park.Get more information on Periyar National Park in Kerala along with major attractions, sightseeing, how to reach and places to visit in and around. Also see top wildlife sanctuaries in Kerala.</p>
  <h3><b>6. Manas National Park</b></h3>
  <div class="wp-block-image">
      <img src="images/mn.jpg" style="width:700px;height:350px;">
  </div>
  <p>Manas National Park has listed itself as a UNESCO Natural World Heritage Site. It is located in Assam and is considered one of the best biodiversity spots in North East India. The park is a safe shelter for many rare and species that are at the verge of extinction. It is a biosphere reserve which houses tigers, elephants, golden langur, red panda, rhinos, etc. The river Manas flows through the national park by which it has got its name. Apart from wildlife, you can indulge in river rafting activity, tour the tea plantations, and enjoy jeep safari to spot wild animals species. </p>
