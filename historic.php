       <p>Constructed by Shah Jahan as a symbol of love, he built the Taj Mahal for his late wife Mumtaz. This iconic structure is the crown jewel of India. Built using the finest marble, the Taj Mahal has interesting stories to tell.For all the information, book a guided tour that costs around Rs. 200 for Indian tourists. For a great experience, visit the full moon Taj-viewing experiences; it is something truly worth experiencing.</p>

  <h3><b>2. Red Fort</b></h3>
  <div class="wp-block-image">
      <img src="images/rf.jpg" style="width:700px;height:350px;">
  </div>
  <p>Ask anyone in Delhi about a must-visit place and they will tell you to drop by at the Red Fort. Built by Shah Jahan, this fort is a testament to the glorious architectural pride of the Mughal Empire. Visit the fort and soak in the tremendous historic ambience.There’s a lot to see at the fort and for the best experience hire a guide. If you are visiting in the evening, do not forget to sit down for the sound and light show that is narrated by the legendary Bollywood actor Amitabh Bachchan.</p>
  <h3><b>3. Fatehpur Skri</b></h3>
  <div class="wp-block-image">
      <img src="images/fateh.jpg" style="width:700px;height:350px;">
  </div>
  <p>
    This was one of the first planned cities in the Mughal Era. Although it was short-lived, it still plays a crucial role in Mughal history. The city was once brimming with buzzing palaces and public buildings. Today, these are all tourist attractions.<br>In Fatehpur Sikri, don’t forget to browse through the many beautiful monuments with decorative artwork on their pillars.</p>
 <h3><b>4. Victoria Memorial</b></h3>
  <div class="wp-block-image">
      <img src="images/victo.jpg" style="width:700px;height:350px;">
  </div>
  <p>The Victoria Memorial in Kolkata was built to commemorate Britain’s 25 years of rule over India. Made of white marble, the building is an epitome of grace and beauty.The stand out feature of the Victoria Memorial is the sixteen-foot tall bronze statue of victory atop the building. The best time to visit is at night when the entire building is illuminate</p>
 <h3><b>5. Hawa Mahal</b></h3>
  <div class="wp-block-image">
      <img src="images/hm.jpg" style="width:700px;height:350px;">
  </div>
  <p>The Hawa Mahal was built by Maharaja Sawai Pratap Singh in the shape of a crown because he was a strong devotee of Lord Krishna. Also called the Palace of Winds, this towering building features many intricately designed windows. Access to the top is permitted and you can catch stunning views of Jaipur’s skyline. When you visit, do not forget to drop by at the small museum inside the Hawa Mahal, which displays excellent paintings and artwork.</p>
   
