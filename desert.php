      <h1><b>Deserts</b></h1>
      <p>Deserts in India offer some of the most spectacular landscapes, full of visual inspirations. From the vast expanse of Thar Desert to the incredible cold desert of Ladakh, India packs an assortment of desert destinations for intrepid travelers.The Thar Desert, also known as the Great Indian Desert, is the 17th largest desert on earth. A major part of the Thar Desert is in Rajasthan. As it extends into Gujarat, the golden sand desert transforms into a dazzling white salt desert.The northern region of the country also boasts some of the astonishing desert terrains. Nestled high in the Himalayas, Spiti Valley in Himachal Pradesh and Ladakh are known as cold deserts. Both the places don’t get much rain due to their position on the leeward side of the Himalayas.</p>

