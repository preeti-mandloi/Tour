  <h2>Place for visit in india</h2>

  <h3><b>1. Goa </b></h3>
    <div class="wp-block-image">
      <img src="images/go.jpg" style="width:700px;height:300px;">
    </div>
       <p>When in doubt, go Goa!  One cannot really go wrong with India’s party capital. A place where people can be seen sunbathing, swimming, parasailing or relaxing in their hammocks sipping a cold beer all year round, Goa is my first pick. In fact, Goa is the best place to kick-start your beach blast in advance with Sunburn Goa this December! Sunburn is when a conglomerate of party mongers and DJs from all over the world get together at the Candolim beach for the grandest New Year beach party. Late night parties, hypnotic music and people dancing away; Sunburn exponentially increases the fun quotient.</p>

  <h3><b>2. Ganpatipule, Maharashtra</b></h3>
  <div class="wp-block-image">
      <img src="images/gp.jpg" style="width:700px;height:350px;">
  </div>
  <p>A tiny coastal town in Maharashtra, Ganpatipule has a grand beach, one of the most spectacular on the Konkan coast. The beach has distinctive red sand and shallow water, making it ideal for swimming. Ganpatipule can be visited en route to Ratnagiri and is just 35 kilometres from the famous Jaigad Fort, which offers a panoramic view of the sea and villages from the cliff. Ganpatipule is also famous for the Swaymbhu Ganesh temple where Lord Ganesh’s statue is believed to have originated from the ground.</p>
  <h3><b>3. Marari, Kerala</b></h3>
  <div class="wp-block-image">
      <img src="images/mk.jpg" style="width:700px;height:350px;">
  </div>
  <p>
    Kerala and its exotic beauty is an open secret. This vibrant state draws tourists from over the world who readily succumb to its magnificence. This time round, visit the lesser known treasures in Kerala like Marari Beach in Alleppey district. A sleepy town brimming with beauty on all four sides, this town is your paradisiacal retreat from the world. In Marari, you can stay at the Marari Beach Resort, a lavish property that subtly blends modern day comforts and a range of recreational activities with surrounding natural beauty.</p>
 <h3><b>4. Kovalam</b></h3>
  <div class="wp-block-image">
      <img src="images/Kovalam.jpg" style="width:700px;height:350px;">
  </div>
  <p>Further down south from Marari, another rejuvenating beach holiday destination in Kerala is Kovalam. Basically a fishing village, Kovalam has garnered a lot of tourist attention in recent years and its famous Eve and Lighthouse beaches are pristine and clean lined with coconut trees of one side. You can opt for a highly rewarding cruise early in the morning with the fishermen or hire a surfboard and set on a mission to control the waves!</p>
 <h3><b>5. Havelock, Andaman</b></h3>
  <div class="wp-block-image">
      <img src="images/ha.jpg" style="width:700px;height:350px;">
  </div>
  <p>Sparkling white sand, turquoise waters, vibrant coral reefs and a calmness that fills the air. Add to this, scuba diving, snorkelling, trekking, elephant rides and a lot more - your ideal beach holiday this New Year is ready! Havelock in Andaman promises a pleasant and highly rejuvenating holiday where celebration comes from being one with nature. The Radhanagar and Elephant beaches are the best places to go for a swim in the warm water and enjoy water sports. Seeking a break from your metropolitan lifestyle? Havelock is must on your Beach Holidays list. Read more about Andaman beaches here.</p>
  <h3><b>6. Pondicherry</b></h3>
  <div class="wp-block-image">
      <img src="images/pandu.jpg" style="width:700px;height:350px;">
  </div>
  <p>One of the best known places in India for meditation and relaxation, Pondicherry or Puducherry is famous as India’s French connection. The erstwhile French colony retains French culture and architecture and is definitely a unique place to visit. Famous for the Sri Aurobindo Ashram in neighbouring Auroville where thousands go to seek solace, a well-maintained waterfront is just completes the package of a beach holiday. Beach activities in Pondicherry include camping at the beach, sunbathing, canoeing, kayaking and backwater sailing.</p>
   
